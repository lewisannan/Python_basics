#Arithmetic Operations - Simple calculations

x = 34
y = 20
print(x + y)
print(x - y)
print(x * y)
print(x / y)
print(x % y) #Modulus - returns the remainder after division


#Comparison Operators

print(x < y)
print(x > y)
print(x <= y)
print(x >= y)
print(x == y) #equal to
print(x != y) #not equal to

#Assignment Operators

number = 10
print(number)

#Operator Precedence
number +=2
print(number)
number %=2
print(number)

#Logical Operators - and, or, not
num1 = 60
num2 = 12

print(num1 == num2 or num1 > num2)
print(num1 == num2 and num1 > num2)
print(not(num1 == num2 or num1 > num2))

